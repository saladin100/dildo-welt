          <h2 class="h2-responsive" id="anwendung">Anwendung</h2>

          <p>
           Glas Dildos sind normalerweise aus Borosilikatglas gefertigt. Diese Glasart ist extrem chemie- und temperaturbeständiges und stabil und wird zum Beispiel für Laborgeräte verwendet.
         </p>

         <p>
           Dementsprechend sicher ist das Material. Das heißt man braucht sich keine Sorgen zu machen, dass das Glas im Körper brechen oder splittern könnte. Selbst wenn der Dildo mal auf harten Boden fällt oder man ihn härter angeht, passiert dem Dildo nichts. Man muss solche Dildos schon wirklich kaputtmachen wollen, um sie zu zerstören.
         </p>

         <p>
           Gerade wenn ein Dildo aber mal auf den Fließenboden gefallen ist, sollte man vor der Benutzung  zur Sicherheit nochmal kontrollieren ob die Oberfläche glatt ist und keine Splitter oder Risse im Glas sind.
         </p>

         <p>
           Außerdem ist Glas sehr gut wärmeleitend. D.h. wenn es in Kontakt mit dem Körper kommt kann es sich kalt anfühlen, da die Körperwärme schnell abgeleitet wird. Für manche ist das ein erregender Effekt. Alle anderen haben die Möglichkeit den Dildo vor der Benutzung 1-2 Minuten in warmes Wasser zu legen.
         </p>

         <p>
           Oder man geht in die extreme und experimentiert mit Temperaturspielen. Dazu den Dildo vor der Benutzung einfach in eiskaltes (oder heißes) Wasser legen, oder kurz auf die Heizung oder den Kühlschrank. Es gilt aber Vorsicht bei extremen Temperaturen: Bevor man den Glas Dildo dann in den intimen Zonen einsetzt, am besten erstmal durch andere Körper teile herantasten, um Verletzungen und Überreizungen vorzubeugen.
         </p>

